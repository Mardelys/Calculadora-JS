//Se declara la clase calculadora quien va a contener las operaciones delcarando las funciones de suma,resta,multiplicación y división de un numero y que va a retornar al llevarse a cabo la función//
class Calculadora{
    sumar(num1,num2) {
        return num1 + num2;
    }
    restar(num1,num2) {
        return num1 - num2;
    }
    dividir(num1,num2) {
        return num1 / num2;
    }
    multiplicar(num1,num2) {
        return num1 * num2;
    }
}
